# ── Session-scoped Engine to prevent event loop issues ────────────────────────

@pytest_asyncio.fixture
async def engine() -> AsyncGenerator[AsyncEngine, None]:
    """
    Function-scoped engine using NullPool.
    Recreated per test to avoid 'Event loop is closed' conflicts with pytest-asyncio.
    NullPool has no persistent connections so there is no performance overhead.
    """
    eng = create_async_engine(
        _async_url,
        echo=False,
        connect_args=_connect_args,
        poolclass=NullPool,
    )
    yield eng
    await eng.dispose()


@pytest_asyncio.fixture
async def db_session(engine: AsyncEngine) -> AsyncGenerator[AsyncSession, None]:
    """
    Provides a fresh AsyncSession per test, with automatic rollback.
    This prevents test data from persisting in NeonDB.
    """
    session_factory = async_sessionmaker(
        bind=engine,
        class_=AsyncSession,
        expire_on_commit=False,
        autocommit=False,
        autoflush=False,
    )
    async with session_factory() as session:
        yield session
        await session.rollback()


